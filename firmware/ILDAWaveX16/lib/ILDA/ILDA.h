typedef struct {
  int16_t x, y;
  uint16_t r, g, b;
} Point;

typedef struct __attribute__((packed)) {
  char ilda[4];
  uint8_t reserved1[3];
  uint8_t format;
  char frame_name[8];
  char company_name[8];
  uint16_t records;
  uint16_t frame_number;
  uint16_t total_frames;
  uint8_t projector_number;
  uint8_t reserved2;
} ILDA_Header_t;

typedef struct {
  ILDA_Header_t header; // header of the current file
  File file; // file handle for streaming
  uint32_t data_offset; // start of record data
  uint32_t next_offset; // next record offset for chunked reading
  uint16_t current_frame_idx; // current frame index
  uint16_t current_record_idx; // current record within frame
  uint8_t bytes_per_record; // size of each record in bytes
} ILDA_Stream;

// Format 0 - 3D Indexed: XX YY ZZ S C
// Format 1 - 2D Indexed: XX YY S C
// Format 2 - Color Palette: R G B
// Format 4 - 3D True Color: XX YY ZZ S B G R
// Format 5 - 2D True Color: XX YY S B G R
typedef struct __attribute__((packed)) {
  int16_t x;
  int16_t y;
  int16_t z;
  uint8_t status_code;
  union {
      uint8_t color_index;
      struct {
          uint8_t blue;
          uint8_t green;
          uint8_t red;
      };
  };
} ILDA_Record_t;

typedef struct {
  ILDA_Record_t *records;
  uint16_t number_records;
  uint8_t format;
} ILDA_Frame_t;

